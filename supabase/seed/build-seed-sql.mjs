// Generates 002_seed_catalogues.sql from the three seed CSVs.
// Run: node supabase/seed/build-seed-sql.mjs
// Idempotent output (ON CONFLICT ... DO UPDATE) — safe to re-apply.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));

// --- minimal RFC4180 CSV parser (quoted fields, embedded commas, "" escapes) ---
function parseCSV(text) {
  const rows = [];
  let row = [], field = '', inQuotes = false;
  text = text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n') { row.push(field); rows.push(row); row = []; field = ''; }
    else field += c;
  }
  if (field !== '' || row.length > 0) { row.push(field); rows.push(row); }
  return rows;
}

function records(text) {
  const rows = parseCSV(text).filter(r => r.some(c => c.trim() !== ''));
  const header = rows[0].map(h => h.trim());
  return rows.slice(1).map(r => Object.fromEntries(header.map((h, i) => [h, (r[i] ?? '').trim()])));
}

// --- SQL value helpers ---
const sqlStr = v => `'${String(v).replace(/'/g, "''")}'`;
const strOrNull = v => (v === '' || v == null) ? 'NULL' : sqlStr(v);
const num = v => {
  if (v === '' || v == null) return 'NULL';
  const n = Number(v);
  if (!Number.isFinite(n)) throw new Error(`Bad number: "${v}"`);
  return String(n);
};
const arr = v => {
  if (v === '' || v == null) return 'NULL';
  const items = v.split(/,\s+/).map(s => s.trim()).filter(Boolean);
  return items.length ? `ARRAY[${items.map(sqlStr).join(', ')}]::text[]` : 'NULL';
};

const read = f => records(readFileSync(join(here, f), 'utf8'));
const compounds = read('compounds.csv');
const biomarkers = read('biomarkers.csv');
const refRanges = read('reference_ranges.csv');

let out = `-- AUTO-GENERATED by build-seed-sql.mjs — do not edit by hand.
-- Source: compounds.csv, biomarkers.csv, reference_ranges.csv
-- Re-generate: node supabase/seed/build-seed-sql.mjs
-- Idempotent: ON CONFLICT ... DO UPDATE keeps re-seeds clean.
-- Requires migration 001_catalogue_extend.sql (new enum values + table) first.

`;

// ===== compounds =====
out += '-- ===== compounds =====\n';
out += 'INSERT INTO compounds (name, category, default_unit, default_route, default_inventory_type, aliases, half_life_hours) VALUES\n';
out += compounds.map(c =>
  `  (${sqlStr(c.name)}, ${sqlStr(c.category)}, ${sqlStr(c.default_unit)}, ${sqlStr(c.default_route)}, ${sqlStr(c.default_inventory_type)}, ${arr(c.aliases)}, ${num(c.half_life_hours)})`
).join(',\n');
out += `\nON CONFLICT (name) DO UPDATE SET
  category = EXCLUDED.category,
  default_unit = EXCLUDED.default_unit,
  default_route = EXCLUDED.default_route,
  default_inventory_type = EXCLUDED.default_inventory_type,
  aliases = EXCLUDED.aliases,
  half_life_hours = EXCLUDED.half_life_hours;

`;

// ===== biomarkers =====
out += '-- ===== biomarkers =====\n';
out += 'INSERT INTO biomarkers (name, category, canonical_unit, alt_unit, alt_unit_factor, ref_low_male, ref_high_male, ref_low_female, ref_high_female, aliases) VALUES\n';
out += biomarkers.map(b =>
  `  (${sqlStr(b.name)}, ${sqlStr(b.category)}, ${sqlStr(b.canonical_unit)}, ${strOrNull(b.alt_unit)}, ${num(b.alt_unit_factor)}, ${num(b.ref_low_male)}, ${num(b.ref_high_male)}, ${num(b.ref_low_female)}, ${num(b.ref_high_female)}, ${arr(b.aliases)})`
).join(',\n');
out += `\nON CONFLICT (name) DO UPDATE SET
  category = EXCLUDED.category,
  canonical_unit = EXCLUDED.canonical_unit,
  alt_unit = EXCLUDED.alt_unit,
  alt_unit_factor = EXCLUDED.alt_unit_factor,
  ref_low_male = EXCLUDED.ref_low_male,
  ref_high_male = EXCLUDED.ref_high_male,
  ref_low_female = EXCLUDED.ref_low_female,
  ref_high_female = EXCLUDED.ref_high_female,
  aliases = EXCLUDED.aliases;

`;

// ===== reference_ranges (age/sex-banded; biomarker_id resolved by name) =====
out += '-- ===== reference_ranges (age/sex-banded) =====\n';
out += 'INSERT INTO reference_ranges (biomarker_id, sex, age_min, age_max, unit, ref_low, ref_high, source)\n';
out += 'SELECT b.id, v.sex, v.age_min, v.age_max, v.unit, v.ref_low, v.ref_high, v.source\n';
out += 'FROM (VALUES\n';
out += refRanges.map(r => {
  const sex = (r.sex === '' || r.sex.toLowerCase() === 'any')
    ? 'NULL::sex_type'
    : `${sqlStr(r.sex.toLowerCase())}::sex_type`;
  return `  (${sqlStr(r.marker)}, ${sex}, ${num(r.age_min)}::smallint, ${num(r.age_max)}::smallint, ${sqlStr(r.unit)}, ${num(r.ref_low)}::numeric, ${num(r.ref_high)}::numeric, ${sqlStr(r.source)})`;
}).join(',\n');
out += `
) AS v(marker, sex, age_min, age_max, unit, ref_low, ref_high, source)
JOIN biomarkers b ON b.name = v.marker
ON CONFLICT ON CONSTRAINT reference_ranges_band_unique DO UPDATE SET
  unit = EXCLUDED.unit,
  ref_low = EXCLUDED.ref_low,
  ref_high = EXCLUDED.ref_high,
  source = EXCLUDED.source;
`;

writeFileSync(join(here, '002_seed_catalogues.sql'), out, 'utf8');
console.log(`Generated 002_seed_catalogues.sql: ${compounds.length} compounds, ${biomarkers.length} biomarkers, ${refRanges.length} reference ranges`);
