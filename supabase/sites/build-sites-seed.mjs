// Generates 002_seed_injection_sites.sql from injection_sites.csv.
// Run: node supabase/sites/build-sites-seed.mjs
// Idempotent output (ON CONFLICT (id) DO UPDATE) — safe to re-apply.
//
// Same pattern as supabase/seed/build-seed-sql.mjs (compounds/biomarkers/markers):
// the CSV is the single source of truth; this regenerates the seed migration.
// Requires 001_injection_site_catalogue.sql (the table) applied first.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));

// --- minimal RFC4180 CSV parser (quoted fields, embedded commas, "" escapes) ---
function parseCSV(text) {
  const rows = [];
  let row = [], field = '', inQuotes = false;
  text = text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ',') { row.push(field); field = ''; }
    else if (c === '\n') { row.push(field); rows.push(row); row = []; field = ''; }
    else field += c;
  }
  if (field !== '' || row.length > 0) { row.push(field); rows.push(row); }
  return rows;
}

function records(text) {
  const rows = parseCSV(text).filter(r => r.some(c => c.trim() !== ''));
  const header = rows[0].map(h => h.trim());
  return rows.slice(1).map(r => Object.fromEntries(header.map((h, i) => [h, (r[i] ?? '').trim()])));
}

// --- SQL value helpers ---
const sqlStr = v => `'${String(v).replace(/'/g, "''")}'`;
const num = v => {
  if (v === '' || v == null) throw new Error(`Missing number: "${v}"`);
  const n = Number(v);
  if (!Number.isFinite(n)) throw new Error(`Bad number: "${v}"`);
  return String(n);
};

const sites = records(readFileSync(join(here, 'injection_sites.csv'), 'utf8'));

let out = `-- AUTO-GENERATED by build-sites-seed.mjs — do not edit by hand.
-- Source: injection_sites.csv
-- Re-generate: node supabase/sites/build-sites-seed.mjs
-- Idempotent: ON CONFLICT (id) DO UPDATE keeps re-seeds clean.
-- Requires migration 001_injection_site_catalogue.sql (the table) first.

-- ===== injection_sites (read-only catalogue) =====
INSERT INTO injection_sites (id, label, route, side, aspect, x, y, sort_order) VALUES
`;
out += sites.map(s =>
  `  (${sqlStr(s.id)}, ${sqlStr(s.label)}, ${sqlStr(s.route)}::admin_route, ${sqlStr(s.side)}, ${sqlStr(s.aspect)}, ${num(s.x)}, ${num(s.y)}, ${num(s.sort_order)})`
).join(',\n');
out += `\nON CONFLICT (id) DO UPDATE SET
  label = EXCLUDED.label,
  route = EXCLUDED.route,
  side = EXCLUDED.side,
  aspect = EXCLUDED.aspect,
  x = EXCLUDED.x,
  y = EXCLUDED.y,
  sort_order = EXCLUDED.sort_order;
`;

writeFileSync(join(here, '002_seed_injection_sites.sql'), out, 'utf8');
console.log(`Generated 002_seed_injection_sites.sql: ${sites.length} injection sites`);
